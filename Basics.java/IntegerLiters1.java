public class IntegerLiters1 {
    public static void main (String [] args ){
        
        int a = 104 ; // Decimal Integer Literals
        int b = 0150 ;  // Octal Integer Literals
        int c = 0x68 ;  // Hexa Integer Literals
        int d = 0b1101000 ; // Binary Integer Literals

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    
    }
}