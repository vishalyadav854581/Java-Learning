/*Swapping Of the Number Using Third Variable  */

import java.util.*;
public class X_Loop_Q8 {
    public static void main (String [] args){

int number_1 =  10 ;
int number_2 =  20 ;
int temp ;

// Swapping the Value
temp = number_1 ;
number_1 = number_2 ;
number_2 = temp ;

System.out.println("Value of Number 1 : "+number_1);
System.out.println("Value of Number 2 : "+number_2);
 
    }
    
}
